  static bool isReserved(String username) {
    final lower = username.toLowerCase();
    if (platformTerms.contains(lower)) return true;
    return _isReservedCountryNamespace(lower);
  }

  static bool _isReservedCountryNamespace(String lower) {
    if (_countrySlugs.contains(lower)) return true;
    for (final country in _countrySlugs) {
      for (final suffix in _governmentSuffixes) {
        if (lower == '${country}_$suffix') return true;
      }
    }
    return false;
  }
