import 'package:flutter/material.dart';

import '../../../../core/constants/app_colors.dart';
import '../../../../core/theme/app_radius.dart';
import '../../../../core/theme/app_spacing.dart';

/// Shows the three-dots "more options" bottom sheet: Download, Report, Not Interested,
/// then Captions, Playback speed, Quality, Manage preferences, Why you're seeing this.
void showReelMoreOptionsSheet(
  BuildContext context, {
  required String reelId,
  String playbackSpeed = 'Normal',
  String quality = 'Auto (1080p HD)',
  VoidCallback? onDownload,
  VoidCallback? onReport,
  VoidCallback? onNotInterested,
  VoidCallback? onCaptions,
  VoidCallback? onPlaybackSpeed,
  VoidCallback? onQuality,
  VoidCallback? onManagePreferences,
  VoidCallback? onWhyThisPost,
}) {
  showModalBottomSheet<void>(
    context: context,
    isScrollControlled: true,
    backgroundColor: Colors.transparent,
    builder: (context) => _ReelMoreOptionsSheet(
      reelId: reelId,
      playbackSpeed: playbackSpeed,
      quality: quality,
      onDownload: onDownload,
      onReport: onReport,
      onNotInterested: onNotInterested,
      onCaptions: onCaptions,
      onPlaybackSpeed: onPlaybackSpeed,
      onQuality: onQuality,
      onManagePreferences: onManagePreferences,
      onWhyThisPost: onWhyThisPost,
    ),
  );
}

abstract final class _Layout {
  static const double dragHandleWidth = 36;
  static const double dragHandleHeight = 4;
}

class _ReelMoreOptionsSheet extends StatelessWidget {
  const _ReelMoreOptionsSheet({
    required this.reelId,
    required this.playbackSpeed,
    required this.quality,
    this.onDownload,
    this.onReport,
    this.onNotInterested,
    this.onCaptions,
    this.onPlaybackSpeed,
    this.onQuality,
    this.onManagePreferences,
    this.onWhyThisPost,
  });

  final String reelId;
  final String playbackSpeed;
  final String quality;
  final VoidCallback? onDownload;
  final VoidCallback? onReport;
  final VoidCallback? onNotInterested;
  final VoidCallback? onCaptions;
  final VoidCallback? onPlaybackSpeed;
  final VoidCallback? onQuality;
  final VoidCallback? onManagePreferences;
  final VoidCallback? onWhyThisPost;

  static const Color _sectionBg = Color(0xFF351F35);

  @override
  Widget build(BuildContext context) {
    return DraggableScrollableSheet(
      initialChildSize: 0.5,
      minChildSize: 0.3,
      maxChildSize: 0.75,
      builder: (context, scrollController) {
        return Container(
          decoration: const BoxDecoration(
            color: AppColors.sheetBackground,
            borderRadius: BorderRadius.vertical(top: Radius.circular(AppRadius.pill)),
          ),
          child: Column(
            mainAxisSize: MainAxisSize.min,
            children: [
              _DragHandle(),
              Padding(
                padding: const EdgeInsets.symmetric(horizontal: AppSpacing.md, vertical: AppSpacing.sm),
                child: Row(
                  children: [
                    Expanded(
                      child: _ActionButton(
                        icon: Icons.download_rounded,
                        label: 'Download',
                        onTap: () {
                          Navigator.of(context).pop();
                          onDownload?.call();
                        },
                      ),
                    ),
                    const SizedBox(width: AppSpacing.sm),
                    Expanded(
                      child: _ActionButton(
                        icon: Icons.report_outlined,
                        label: 'Report',
                        iconColor: AppColors.pink,
                        labelColor: AppColors.pink,
                        onTap: () {
                          Navigator.of(context).pop();
                          onReport?.call();
                        },
                      ),
                    ),
                    const SizedBox(width: AppSpacing.sm),
                    Expanded(
                      child: _ActionButton(
                        icon: Icons.favorite_border,
                        label: 'Not Interested',
                        onTap: () {
                          Navigator.of(context).pop();
                          onNotInterested?.call();
                        },
                      ),
                    ),
                  ],
                ),
              ),
              Expanded(
                child: ListView(
                  controller: scrollController,
                  padding: const EdgeInsets.symmetric(horizontal: AppSpacing.md, vertical: AppSpacing.xs),
                  children: [
                    _Section(
                      backgroundColor: _sectionBg,
                      borderRadius: AppRadius.input,
                      children: [
                        _SettingTile(
                          icon: Icons.closed_caption_outlined,
                          label: 'Captions and translations',
                          onTap: () {
                            Navigator.of(context).pop();
                            onCaptions?.call();
                          },
                        ),
                        _SettingTile(
                          icon: Icons.speed_rounded,
                          label: 'Playback speed',
                          trailing: playbackSpeed,
                          onTap: () {
                            Navigator.of(context).pop();
                            onPlaybackSpeed?.call();
                          },
                        ),
                        _SettingTile(
                          icon: Icons.hd_outlined,
                          label: 'Quality',
                          trailing: quality,
                          onTap: () {
                            Navigator.of(context).pop();
                            onQuality?.call();
                          },
                        ),
                      ],
                    ),
                    const SizedBox(height: AppSpacing.sm),
                    _Section(
                      backgroundColor: _sectionBg,
                      borderRadius: AppRadius.input,
                      children: [
                        _SettingTile(
                          icon: Icons.tune_rounded,
                          label: 'Manage content preferences',
                          onTap: () {
                            Navigator.of(context).pop();
                            onManagePreferences?.call();
                          },
                        ),
                        _SettingTile(
                          icon: Icons.info_outline_rounded,
                          label: "Why you're seeing this post",
                          onTap: () {
                            Navigator.of(context).pop();
                            onWhyThisPost?.call();
                          },
                        ),
                      ],
                    ),
                    const SizedBox(height: AppSpacing.xl),
                  ],
                ),
              ),
            ],
          ),
        );
      },
    );
  }
}

class _DragHandle extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.only(top: AppSpacing.storyItem, bottom: AppSpacing.xs),
      child: Center(
        child: Container(
          width: _Layout.dragHandleWidth,
          height: _Layout.dragHandleHeight,
          decoration: BoxDecoration(
            color: Colors.white.withValues(alpha: 0.5),
            borderRadius: BorderRadius.circular(2),
          ),
        ),
      ),
    );
  }
}

class _ActionButton extends StatelessWidget {
  const _ActionButton({
    required this.icon,
    required this.label,
    this.iconColor,
    this.labelColor,
    required this.onTap,
  });

  final IconData icon;
  final String label;
  final Color? iconColor;
  final Color? labelColor;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    final color = iconColor ?? Colors.white;
    final textColor = labelColor ?? Colors.white;
    return Material(
      color: Colors.white.withValues(alpha: 0.12),
      borderRadius: BorderRadius.circular(AppRadius.input),
      child: InkWell(
        onTap: onTap,
        borderRadius: BorderRadius.circular(AppRadius.input),
        child: Padding(
          padding: const EdgeInsets.symmetric(vertical: AppSpacing.sm),
          child: Column(
            mainAxisSize: MainAxisSize.min,
            children: [
              Icon(icon, size: 24, color: color),
              const SizedBox(height: AppSpacing.xs),
              Text(
                label,
                textAlign: TextAlign.center,
                maxLines: 1,
                overflow: TextOverflow.ellipsis,
                style: TextStyle(color: textColor, fontSize: 12, fontWeight: FontWeight.w500),
              ),
            ],
          ),
        ),
      ),
    );
  }
}

class _Section extends StatelessWidget {
  const _Section({
    required this.backgroundColor,
    required this.borderRadius,
    required this.children,
  });

  final Color backgroundColor;
  final double borderRadius;
  final List<Widget> children;

  @override
  Widget build(BuildContext context) {
    return Container(
      decoration: BoxDecoration(
        color: backgroundColor,
        borderRadius: BorderRadius.circular(borderRadius),
      ),
      child: Column(
        mainAxisSize: MainAxisSize.min,
        children: children,
      ),
    );
  }
}

class _SettingTile extends StatelessWidget {
  const _SettingTile({
    required this.icon,
    required this.label,
    this.trailing,
    required this.onTap,
  });

  final IconData icon;
  final String label;
  final String? trailing;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    return InkWell(
      onTap: onTap,
      child: Padding(
        padding: const EdgeInsets.symmetric(horizontal: AppSpacing.md, vertical: AppSpacing.sm),
        child: Row(
          children: [
            Icon(icon, size: 22, color: Colors.white70),
            const SizedBox(width: AppSpacing.md),
            Expanded(
              child: Text(
                label,
                style: const TextStyle(
                  color: Colors.white,
                  fontSize: 16,
                  fontWeight: FontWeight.w400,
                ),
              ),
            ),
            if (trailing != null) ...[
              Text(
                trailing!,
                style: TextStyle(
                  color: Colors.white.withValues(alpha: 0.6),
                  fontSize: 14,
                ),
              ),
              const SizedBox(width: AppSpacing.xs),
            ],
            Icon(
              Icons.chevron_right,
              size: 20,
              color: Colors.white.withValues(alpha: 0.5),
            ),
          ],
        ),
      ),
    );
  }
}
